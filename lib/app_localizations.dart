  static Locale parseLocaleCode(String code) {
    final c = code.trim();
    if (c.isEmpty) return const Locale('en');

    final normalized = c.replaceAll('-', '_');
    final parts = normalized.split('_');
    if (parts.length == 1) return Locale(parts[0].toLowerCase());

    final lang = parts[0].toLowerCase();
    final country = parts[1].toUpperCase();
    return Locale(lang, country);
  }

  String get _key {
    final lang = locale.languageCode.toLowerCase();
    final cc = (locale.countryCode ?? '').toUpperCase();
    return cc.isEmpty ? lang : '${lang}_$cc';
  }

